<template>
<div class="mk-follow-page">
</div>
</template>

<script lang="ts">
import { defineComponent } from 'vue';
import * as os from '@/os';

export default defineComponent({
	created() {
		const acct = new URL(location.href).searchParams.get('acct');
		if (acct == null) return;

		const dialog = os.dialog({
			type: 'waiting',
			text: this.$t('fetchingAsApObject') + '...',
			showOkButton: false,
			showCancelButton: false,
			cancelableByBgClick: false
		});

		if (acct.startsWith('https://')) {
			os.api('ap/show', {
				uri: acct
			}).then(res => {
				if (res.type == 'User') {
					this.follow(res.object);
				} else {
					os.dialog({
						type: 'error',
						text: 'Not a user'
					}).then(() => {
						window.close();
					});
				}
			}).catch(e => {
				os.dialog({
					type: 'error',
					text: e
				}).then(() => {
					window.close();
				});
			}).finally(() => {
				dialog.close();
			});
		} else {
			os.api('users/show', parseAcct(acct)).then(user => {
				this.follow(user);
			}).catch(e => {
				os.dialog({
					type: 'error',
					text: e
				}).then(() => {
					window.close();
				});
			}).finally(() => {
				dialog.close();
			});
		}
	},

	methods: {
		async follow(user) {
			const { canceled } = await os.dialog({
				type: 'question',
				text: this.$t('followConfirm', { name: user.name || user.username }),
				showCancelButton: true
			});

			if (canceled) {
				window.close();
				return;
			}
			
			os.api('following/create', {
				userId: user.id
			}).then(() => {
				os.success().then(() => {
					window.close();
				});
			}).catch(e => {
				os.dialog({
					type: 'error',
					text: e
				}).then(() => {
					window.close();
				});
			});
		}
	}
});
</script>
